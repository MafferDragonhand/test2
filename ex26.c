/**************************************************************************************
*		              动态数码管显示实验												  *
实现现象：动态LED向左滚动显示8个字符：19950316。
注意事项：																				  
***************************************************************************************/

#include "reg52.h"			 //此文件中定义了单片机的一些特殊功能寄存器

/*******************************************************************************
* 函 数 名         : time1ms
* 函数功能		   : 在T0工作方式下定时1ms，采用查询方式实现
*******************************************************************************/

void time1ms(unsigned char i)
{
	unsigned char k;
	TMOD = 0x01;
	for(k=0;k<i;k++)
	{	
		TH0 = (65536-1000)/256;
		TL0 = (65536-1000)%256;
		TR0 = 1;
		while(!TF0);
		TF0 = 0;
	}
}

/*******************************************************************************
* 函 数 名         : disp2
* 函数功能		   : 实现8个LED数码管动态向左滚动显示数字：19950316
*******************************************************************************/
void disp2()
{
unsigned char ledmove[]={0x00,0x00,0x00,0x00,0x00,0x00,0x00,
	            					 0x06,0x6f,0x6f,0x6d,0x3f,0x4f,0x06,0x7d,
					            	 0x00,0x00,0x00,0x00,0x00,0x00,0x00};//显示19950316的值

unsigned char com[] = {0x00,0x04,0x08,0x0c,0x10,0x14,0x18,0x1c};

unsigned char i,j,num;
	 for(num=0;num<15;num++)
				for(j=0;j<10;j++)
						for(i=0;i<8;i++)
				{
				P2 = com[i];
				P0 = ledmove[num+i];
				time1ms(1);
				P0 = 0x00;
				}
		
}


/*******************************************************************************
* 函 数 名       : main
* 函数功能		 : 主函数
* 输    入       : 无
* 输    出    	 : 无
*******************************************************************************/
void main()
{	
	while(1)
	{	
		disp2();  //数码管显示函数	
	}		
}
